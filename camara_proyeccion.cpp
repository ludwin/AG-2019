void moverCubo(int direccion){
    switch(direccion){
        case MOV_ARRIBA://Arriba
            if(esValido(posX,posZ-1) && mapa[posZ-1][posX]!=1){
                for (int i = 0; i < tamCubo; i++)
                {
                    zActual-=1;
                    rotx-=4.5;
                    display();
                }
                posZ--;
            }
            break;
        case MOV_ABAJO://Abajo
            if(esValido(posX,posZ+1) && mapa[posZ+1][posX]!=1){
                for (int i = 0; i < tamCubo; i++)
                {
                    zActual+=1;
                    rotx+=4.5;
                    display();
                }
                posZ++;
            }
            break;
        case MOV_IZQUIERDA://Izquierda
            if(esValido(posX-1,posZ) && mapa[posZ][posX-1]!=1){
                for (int i = 0; i < tamCubo; i++){
                    xActual-=1;
                    roty+=4.5;
                    display();
                }
                posX--;
            }
            break;
        case MOV_DERECHA://Derecha
            if(esValido(posX+1,posZ) && mapa[posZ][posX+1]!=1){
                for (int i = 0; i < tamCubo; i++){
                    xActual+=1;
                    roty-=4.5;
                    display();
                }
                posX++;
            }
            break;
    }
}

// Funci√≥n para controlar teclas especiales
void specialKeys( int key, int x, int y )
{

    //  Flecha derecha: aumentar rotaci√≥n 7 grados
    if (key == GLUT_KEY_RIGHT)
        moverCubo(MOV_DERECHA);

    //  Flecha izquierda: rotaci√≥n en eje Y negativo 7 grados
    else if (key == GLUT_KEY_LEFT)
        moverCubo(MOV_IZQUIERDA);
    //  Flecha arriba: rotaci√≥n en eje X positivo 7 grados
    else if (key == GLUT_KEY_UP)
        moverCubo(MOV_ARRIBA);
    //  Flecha abajo: rotaci√≥n en eje X negativo 7 grados
    else if (key == GLUT_KEY_DOWN)
         moverCubo(MOV_ABAJO);

    //  Solicitar actualizaci√≥n de visualizaci√≥n
    display();

}

